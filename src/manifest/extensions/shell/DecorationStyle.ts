export enum DecorationStyle {
  /**
   * No decoration on a application icon
   */
  NONE = 'none',
  /**
   * A simple dot is shown overlayed on application icon if there are notifications
   */
  SIMPLE = 'simple',
  /**
   * A number of unread notifications is shown on application icon if there are notifications.
   */
  FULL = 'full',
}
